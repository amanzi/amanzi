    CHECK_CLOSE((2.2 - 1.3) / 2, s((1.3 + 0.1) / 2), 1.e-10);
    CHECK_CLOSE(2.2 + m * 0.2, s(0.1 + 0.2), 1.e-10);

    CHECK_CLOSE(m, s.Derivative(0.5), 1.e-10);

    // check out of bounds
    CHECK_THROW(s(-100.), Amanzi::Utils::SplinedCurve::Amanzi_exception_SplineOutOfRange);
